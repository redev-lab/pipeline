cov = {a: 0 for a in ATTRS}
for zone, n, date, f, vals in CASES:
    g = read_gosi(DIR + f)
    ex = {"zone_name": zone, "고시번호": n, "고시일자": date,
          "attrs": {a: {"raw": vals[a][0], "label": a, "변경구분": "변경후", "sentence": vals[a][1]} for a in ATTRS}}
    vr = verify_extraction(ex, g["text"], table_rows=g["rows"], grids=g["grids"])
    print(f"{zone} (고시 {n}): {vr['summary']}")
    for a in ATTRS:
        r = vr["results"][a]; ch = r.get("checks", {})
        if r["grade"] == "verified": cov[a] += 1
        print(f"  · {a}: [{r['grade']}] {r['raw']}  (표출처={ch.get('table_provenance')}, 범위={ch.get('in_range')}, 원문={ch.get('in_source')})")
print("\n★개선 후 커버리지(verified):", {a: f"{cov[a]}/5" for a in ATTRS})
