"""우선순위 고시 → 공식 URL 매니페스트 (토지이음 고시목록 인덱스 기반). ★사용자 로컬 실행.

선행: data.go.kr 15083101(토지이음 고시목록) CSV를 받아 아래 경로에 두거나, API로 인덱스 적재.
실행: python scripts/_gosi_urls.py [CSV경로]
→ 13개 우선순위(+완료 5)의 고시번호·고시명·URL 출력 → 그 URL을 클릭해 본문 PDF 수동 다운로드.
"""
import os, sys
sys.path.insert(0, os.getcwd()); sys.stdout.reconfigure(encoding="utf-8")
from redev.data.ingest.gosi_index import load_index, resolve_urls
from redev.paths import DATA

DEFAULT_CSV = DATA / "raw/국토교통부_토지이음_고시목록_20250616.csv"   # 보유 스냅샷(없으면 인자로 경로)

# ★(고시번호, 고시일자, 구역명) — 고시번호는 전국 중복이라 3중 매칭 필수
PRIORITY = [
    ("2024-475", "2024-10-04", "성북1구역"), ("2025-179", "2025-04-03", "상도14구역"),
    ("2025-178", "2025-04-03", "상도15구역"), ("2025-194", "2025-04-10", "석관4구역"),
    ("2025-245", "2025-05-01", "하월곡1구역"), ("2025-456", "2025-08-21", "종암9구역"),
    ("2025-551", "2025-10-02", "본동구역"), ("2024-484", "2024-10-17", "불광8구역"),
    ("2025-163", "2025-03-27", "불광5구역"), ("2025-447", "2025-08-14", "응암동755"),
    ("2025-205", "2025-04-17", "가리봉1구역"), ("2025-226", "2025-04-24", "고척동253"),
    ("2025-287", "2025-05-29", "종암8구역"),
]
DONE = [("2024-448", "2024-09-19", "장위15구역"), ("2025-426", "2025-07-31", "흑석2구역"),
        ("2024-519", "2024-10-31", "청파2구역"), ("2026-18", "2026-02-12", "가리봉1구역"),
        ("2020-133", "2020-07-30", "응암제2구역")]

csv = sys.argv[1] if len(sys.argv) > 1 else str(DEFAULT_CSV)
if not os.path.exists(csv):
    print(f"★CSV 없음: {csv}\n  data.go.kr 15083101 CSV를 받아 이 경로에 두세요(무로그인 다운로드).")
    sys.exit(1)
idx = load_index(csv)
print(f"인덱스 {len(idx):,}행 로드(전국)\n")
for title, tgts in [("=== 1차 배치 13(다운로드 대상) ===", PRIORITY), ("=== 완료 5(참고) ===", DONE)]:
    print(title)
    for m in resolve_urls([{"고시번호": n, "고시일자": d, "구역명": z} for n, d, z in tgts], idx):
        tag = m["url"] or "★미발견(스냅샷 2025-06-16 이후 고시일 수 있음)"
        print(f"  [{m['conf']}] {m['구역명']:<10} {m['고시번호']}  {tag}")
        if m["matched_title"]:
            print(f"        ↳ {str(m['matched_title'])[:55]}")
    print()
