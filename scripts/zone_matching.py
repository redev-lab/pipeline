"""zone_matching.py — 구역명 정규화·매칭 공용 헬퍼 (소스 무관).

신통·정비사업·결정고시(OA-20283)·향후 소스가 모두 쓴다. 한국 정비구역 이름이
제각각(제6 vs 6, 괄호 '(흑석2,9구역)', 접미사 '구역/지구', 촉진 부모/sub)이라
토큰 추출을 한 곳에서 정의한다.

★labels.py 구현 시 redev/ 로 승격 예정(현재는 분석 단계 공용).

────────────────────────────────────────────────────────────────────────────
★오늘(2026-06-11) firm-up에서 잡은 함정 3개 — 다음에 같은 데 안 빠지게:
  1. [자치구 소스] 결정고시 `고시관리코드[:5]`는 자치구가 아니다 — 43,367건 중
     40,517건이 시(서울)레벨 코드 `11000`이고 자치구 코드(11290 등)는 최근분뿐.
     한 구역 이력이 11000(옛)/11290(신)으로 쪼개져 sig 조회가 옛 지정을 놓친다.
     → 자치구 가드는 **동명→구 매핑(지적도)**으로 한다. 고시코드 금지.
  2. ['제' 삼킴] 그리디 `[가-힣]{2,4}`가 '제'를 먹어 '돈암제6'→'돈암제6'(틀림).
     → **비그리디 `[가-힣]{2,4}?`** 로 '제'를 분리('돈암6'). 소스간 제 표기
     불일치 매칭 실패의 원인이었다.
  3. [재건축 누출] '정릉3 주택재건축 정비구역지정'이 '정비구역' 키워드로 통과해
     재개발 토큰('정릉3')과 오매칭. → **is_redev_title에서 '재건축' 배제**.
────────────────────────────────────────────────────────────────────────────
"""
import re

# 정비구역 관련 결정만 거르는 키워드 (지구단위계획·도시계획시설 등 무관 결정 배제)
# '재정비촉진' 포함: 촉진지구 지정·촉진계획 결정도 재개발 결정으로 인정(촉진 부모 회수).
REDEV_TITLE_KW = ("재개발", "정비구역", "정비계획", "정비사업", "재정비촉진")

# 연결자 문자(·, middle-dot 계열) + cp949 깨짐('수색?증산'의 ?)을 일반 규칙으로 제거.
# 특정 케이스 패치가 아니라 "한글 사이 연결자 제거"로 '수색·증산'='수색?증산'='수색증산'.
_CONNECTORS = "·・･‧•∙ㆍ"


def clean_text(s) -> str:
    """제목/구역명에서 연결자·인코딩 깨짐 문자를 제거(소스 무관 정규화 1단계)."""
    s = str(s)
    s = re.sub(f"[{_CONNECTORS}]", "", s)         # middle-dot 계열 제거
    s = re.sub(r"(?<=[가-힣])\?(?=[가-힣])", "", s)  # 한글 사이 cp949 깨짐 ? 제거
    return s
# 재정비촉진 부모지구명 (sub-구역 t 근사용)
PROMOTION_PARENTS = ("수색증산", "흑석", "노량진", "길음", "장위", "한남", "이문휘경",
                     "신정", "방화", "거여마천", "북아현", "미아", "상계")


# 후속단계 고시(지정 아님) — 최초 지정고시 마이닝에서 배제. 수검에서 잡힌 누수:
# 길음1=1998 '시행인가'가 지정으로 오인됨. 시행인가·관리처분 등은 지정 후 단계다.
_LATER_STAGE_KW = ("시행인가", "사업시행계획", "관리처분", "준공", "착공", "이전고시")

# 사이클 완료 고시 — 이게 있으면 그 *이전* 지정은 완료된 옛 사이클(흑석2=1985).
# 수검에서 잡힘: 흑석2 1985 개량재개발→1986 관리처분(완료)→2025 새 촉진. 현재
# 흑석2 t는 새 사이클이어야 하므로, 완료고시 이후의 지정만 현재 사이클로 본다.
CYCLE_DONE_KW = ("관리처분", "준공", "이전고시", "청산")


def cycle_done(title) -> bool:
    """제목이 사이클 완료(관리처분/준공/이전고시) 고시인가."""
    return any(k in str(title) for k in CYCLE_DONE_KW)


def is_redev_title(title) -> bool:
    """제목이 *재개발 정비구역 지정/계획 결정*인가 (지정 마이닝용 필터).

    배제:
      - 재건축('정릉3 주택재건축 정비구역지정' → 재개발 토큰 오매칭 차단).
      - ★후속단계('시행인가'·'관리처분' 등) — 지정이 아니라 지정 *후* 단계 고시.
        수검에서 길음1=1998 시행인가가 지정으로 새던 것을 차단.
    """
    s = str(title)
    if "재건축" in s:
        return False
    if any(k in s for k in _LATER_STAGE_KW):
        return False
    return any(k in s for k in REDEV_TITLE_KW)


def normalize_zone_name(title) -> set:
    """제목/구역명 → 구역 토큰 set ('지역명+번호'). 소스 무관 공용 헬퍼.

    추출:
      - 괄호 안 'XX[제]N[,M]구역'  예: '(흑석2,9구역)' → {흑석2, 흑석9}
      - 선두 'XX[제]N'             예: '돈암제6 주택재개발' → {돈암6}
    정규화: 제N→N, 공백 제거. (한 제목이 여러 구역 참조 가능 → set)
    """
    s = clean_text(title).replace(" ", "")
    toks = set()
    # ([가-힣]{2,4}?) 비그리디 — '제'를 한글부가 삼키지 않게(돈암제6→돈암6).
    for grp in re.findall(r"[\(（]([^\)）]*)[\)）]", s):
        for m in re.finditer(r"([가-힣]{2,4}?)제?(\d+)(?:,(\d+))?구역", grp):
            toks.add(m.group(1) + m.group(2))
            if m.group(3):
                toks.add(m.group(1) + m.group(3))
    m = re.match(r"([가-힣]{2,4}?)제?(\d+)", s)
    if m:
        toks.add(m.group(1) + m.group(2))
    return toks


def region_of(token) -> str:
    """토큰('돈암6'/'응암동700')에서 지역명('돈암'/'응암')만 분리 — 동명→구 가드용.

    trailing '동'/'가' strip: 지번형 토큰('응암동700')의 지역이 '응암동'이 되어
    동맵 키('응암', 동 제거형)와 어긋나는 문제 해결. (단 2자 미만으로 줄면 보존.)
    """
    m = re.match(r"([가-힣]+)", str(token))
    r = m.group(1) if m else ""
    stripped = re.sub(r"(동|가)$", "", r)
    return stripped if len(stripped) >= 2 else r


def parent_of(title) -> str | None:
    """제목이 재정비촉진 sub-구역이면 부모 촉진지구명 반환, 아니면 None."""
    s = clean_text(title)
    if "재정비촉진" not in s:
        return None
    for p in PROMOTION_PARENTS:
        if p in s:
            return p
    return None
